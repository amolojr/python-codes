#program to prompt area and perimeter of a circle
class circle:
    def __init__(self,radius):
     self.radius=radius

def area(self):
    a=3.142*self.radius**2
    return a

def perimeter(self):
    p=3.142*self.radius*2
    return p

c=circle(5)
print(circle.area())
print(c.perimeter())